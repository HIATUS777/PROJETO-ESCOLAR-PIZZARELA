    private void atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarActionPerformed
        produto_fornecer pf = new produto_fornecer();
        if(local.getSelectedItem().equals("")||uf.getSelectedItem().equals("")||quant.getText().equals("")||
          codigo.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Preencha as lacunas");
       }
        else if(!alimentos.isSelected()&& !massa.isSelected()&& !temperos.isSelected()
                && !outros.isSelected()&& !refrigerantes.isSelected()){
            JOptionPane.showMessageDialog(null,"Marque algo");
        }
        else if (pedir.isEnabled()){
            JOptionPane.showMessageDialog(null,"Peça primeiro para poder atualizar");
        }
        else{
      pf.setLocal((String)local.getSelectedItem());
      pf.setUf((String)uf.getSelectedItem());
      pf.setRefrigerantes((Boolean)refrigerantes.isSelected());
      pf.setAlimentos((Boolean)alimentos.isSelected());
      pf.setMassa((Boolean)massa.isSelected());
      pf.setTempero((Boolean)temperos.isSelected());
      pf.setOutros((Boolean)outros.isSelected());
      pf.setMarcas(marcas.getText());
      pf.setQuant((Integer.parseInt(quant.getText())));
      pf.setCod((Integer.parseInt(codigo.getText())));
      
        JOptionPane.showMessageDialog(null, pf.AtualizarF());
       
      local.setSelectedItem(0);
      uf.setSelectedItem(0);
      refrigerantes.setSelected(false);
      marcas.setText("");
     temperos.setSelected(false);
     massa.setSelected(false); 
     alimentos.setSelected(false);
     outros.setSelected(false); 
     codigo.setText("");
     pedir.setEnabled(true);
     quant.setText("");
     finalizar.setEnabled(true);
        }
    
    }//GEN-LAST:event_atualizarActionPerformed

    private void verpedidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verpedidoActionPerformed
          if(evt.getSource()== verpedido){
            try {
                String ccode = JOptionPane.showInputDialog("Digite o código: ");
                BufferedReader bf = new BufferedReader(new FileReader(ccode+"ff.txt"));
                // criando o arquivo de leitura de caracteres
                
                codigo.setText(bf.readLine());
                local.setSelectedItem(bf.readLine());
                marcas.setText(bf.readLine());
                uf.setSelectedItem(bf.readLine());
                quant.setText(bf.readLine());
               alimentos.setSelected(Boolean.parseBoolean(bf.readLine()));
               massa.setSelected(Boolean.parseBoolean(bf.readLine()));
               temperos.setSelected(Boolean.parseBoolean(bf.readLine()));
               refrigerantes.setSelected(Boolean.parseBoolean(bf.readLine()));
                outros.setSelected(Boolean.parseBoolean(bf.readLine()));
                bf.close();
           local.setEnabled(false);
      uf.setEnabled(false);
      refrigerantes.setEnabled(false);
      marcas.setEnabled(false);
     temperos.setEnabled(false);
     massa.setEnabled(false); 
     alimentos.setEnabled(false);
     outros.setEnabled(false); 
     codigo.setEnabled(false);
     pedir.setEnabled(false);
     quant.setEnabled(false);
                
                
            } catch (Exception erro) {
                JOptionPane.showMessageDialog(null, "Erro: " +erro.getMessage());
                Logger.getLogger(cadastro.class.getName()).log(Level.SEVERE, null, erro);
         local.setEnabled(true);
         marcas.setEnabled(true);
         uf.setEnabled(true);
          refrigerantes.setEnabled(true);
          massa.setEnabled(true);
          temperos.setEnabled(true);
          alimentos.setEnabled(true);
          outros.setEnabled(true);
          codigo.setEnabled(true);
          pedir.setEnabled(true);
          quant.setEnabled(true);
            }
        
        }        // TODO add your handling code here:
    }//GEN-LAST:event_verpedidoActionPerformed

