    private void pedirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pedirActionPerformed
       produto_fornecer pf = new produto_fornecer();
        if(local.getSelectedItem().equals("Selecione")||uf.getSelectedItem().equals("Selecione")||quant.getText().equals("")||
          codigo.getText().equals("")){
           JOptionPane.showMessageDialog(null,"Preencha as lacunas");
       }
        else if(!alimentos.isSelected()&& !massa.isSelected()&& !temperos.isSelected()
                && !outros.isSelected()&& !refrigerantes.isSelected()){
            JOptionPane.showMessageDialog(null,"Marque algo");
        }
        else{
      pf.setLocal((String)local.getSelectedItem());
      pf.setUf((String)uf.getSelectedItem());
      pf.setRefrigerantes((Boolean)refrigerantes.isSelected());
      pf.setAlimentos((Boolean)alimentos.isSelected());
      pf.setMassa((Boolean)massa.isSelected());
      pf.setTempero((Boolean)temperos.isSelected());
      pf.setOutros((Boolean)outros.isSelected());
      pf.setMarcas(marcas.getText());
      pf.setQuant((Integer.parseInt(quant.getText())));
      pf.setCod((Integer.parseInt(codigo.getText())));
      
        JOptionPane.showMessageDialog(null, pf.SalvarF());
       
      local.setSelectedItem(0);
      uf.setSelectedItem(0);
      refrigerantes.setSelected(false);
      marcas.setText("");
     temperos.setSelected(false);
     massa.setSelected(false); 
     alimentos.setSelected(false);
     outros.setSelected(false); 
     codigo.setText("");
     quant.setText("");
     finalizar.setEnabled(true);
        }
    
     
      
    }//GEN-LAST:event_pedirActionPerformed

